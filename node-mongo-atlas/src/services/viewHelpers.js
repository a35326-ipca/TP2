// Devolve o texto visível correspondente ao papel do utilizador.
export function roleLabel(role) {
  if (role === "gestor") return "Gestor";
  if (role === "funcionario") return "Funcionário";
  if (role === "aluno") return "Aluno";
  return role ? `${role}` : "";
}

// Define para onde cada tipo de utilizador é encaminhado após login.
export function dashboardPathFor(role) {
  if (role === "gestor") return "/gestor";
  if (role === "funcionario") return "/funcionario";
  return "/aluno";
}

// Cria um item de navegação usado na barra lateral.
export function navItem(href, label, icon) {
  return { href, label, icon };
}

// Monta a navegação de acordo com o papel e o estado do aluno.
export function navForRole(role, studentUnlocked = false) {
  if (role === "gestor") {
    return [
      navItem("/gestor", "Hub", "home"),
      navItem("/perfil", "Perfil", "account"),
      navItem("/gestor/utilizadores", "Utilizadores", "users"),
      navItem("/gestor/cursos", "Cursos", "courses"),
      navItem("/gestor/ucs", "UCs", "units"),
      navItem("/gestor/plano", "Plano", "plan"),
      navItem("/gestor/fichas", "Fichas", "enrollment")
    ];
  }

  if (role === "funcionario") {
    return [
      navItem("/funcionario", "Hub", "home"),
      navItem("/perfil", "Perfil", "account"),
      navItem("/funcionario/matriculas", "Matrículas", "enrollment"),
      navItem("/funcionario/pautas", "Pautas", "grades")
    ];
  }

  const items = [
    navItem("/aluno", "Hub", "home"),
    navItem("/perfil", "Perfil", "account"),
    navItem("/aluno/ficha", "Ficha", "profile")
  ];

  if (studentUnlocked) {
    items.push(navItem("/aluno/matriculas", "Matrícula", "enrollment-student"));
    items.push(navItem("/aluno/notas", "Notas", "grades"));
  }

  return items;
}

// Converte estados da aplicação em classes visuais para badges.
export function statusClass(status) {
  if (["ativo", "aprovado", "aprovada"].includes(status)) return "success";
  if (["inativo", "rejeitado", "rejeitada"].includes(status)) return "danger";
  if (["submetida", "pendente", "rascunho"].includes(status)) return "warning";
  return "neutral";
}

