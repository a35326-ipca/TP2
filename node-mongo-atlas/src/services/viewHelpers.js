// Formata datas para o formato YYYY-MM-DD apresentado nas tabelas.
export function formatDate(value) {
  if (!value) return "-";
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return "-";
  return date.toISOString().slice(0, 10);
}

// Normaliza caminhos de imagens e ficheiros servidos pela pasta statics.
export function assetPath(value) {
  const text = `${value ?? ""}`.trim();
  if (!text) return "";
  if (text.startsWith("/")) return text;
  if (text.startsWith("statics/")) return `/${text}`;
  return text;
}
